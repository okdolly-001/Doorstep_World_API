import UIKit

extension UIColor {
    
    static var accentColor: UIColor {
        get {
            return UIColor(red: 0/255, green: 140/255, blue: 238/255, alpha: 1)
        }
    }
    
    static var darkBackgroundColor: UIColor {
        get {
            return UIColor(red: 144/255, green: 144/255, blue: 144/255, alpha: 1)
        }
    }
    
    static var backgroundColor: UIColor {
        get {
            return UIColor(red: 244/255, green: 244/255, blue: 244/255, alpha: 1)
        }
    }
}
